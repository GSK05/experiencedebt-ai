export function getDashboardData() {
  const totalEds = customers.reduce((sum, customer) => sum + customer.eds, 0);
  const highRiskCustomers = customers.filter((customer) => customer.riskLevel === "high");

  return {
    customers,
    metrics: {
      averageEds: Math.round(totalEds / customers.length),
      highRiskCount: highRiskCustomers.length,
      customerCount: customers.length,
      recoveryAverage: Math.round(
        customers.reduce((sum, customer) => sum + customer.rps, 0) / customers.length,
      ),
    },
    debtTrend,
  };
}
